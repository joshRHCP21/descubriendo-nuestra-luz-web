            </p>
            <p>
              Los cuestionamientos se generan en la adolescencia y yo no fui la excepción. Por ello, puedo decir que es muy fácil alejarse de todo aquello en lo que uno creyó alguna vez, en sus primeros
               años. Sin embargo, las experiencias  -en sus diversos colores y matices-  mantuvieron presente en mí la búsqueda de algo más allá de lo racional y lógico.
            </p>
