            <p>
              Con el paso del tiempo las enseñanzas de la vida hicieron posible que volviera a mis  convicciones iniciales aunque con otra perspectiva, como si se tratara de un panorama diferente. Conocí y 
              empecé a sentir la presencia del amor universal, que es una energía que  trasciende todo y de la que nuestra alma forma parte. Me di cuenta que, más allá de la pertenencia a un credo religioso, 
              existe en cada uno de nosotros, desde el nacimiento, una esencia divina que nunca nos abandona; se trata de nuestra luz interior, señal de divinidad, que se manifiesta cuando sentimos y nos comportamos 
              como lo que realmente somos: almas. Sin embargo, también observé que en algunos casos esa luz interior  se ve opacada por las circunstancias de nuestro entorno y por nuestra conducta.
            </p>
            <p>Comprendí que todo lo que vivimos (como la alegría, el dolor, la enfermedad) tiene como propósito la adquisición de aprendizajes que nos irán conduciendo hacia la toma de conciencia y a la felicidad final. 
              Me di cuenta que el proponerme ser feliz era mi obligación, como debe serlo para todos los seres humanos. Y comprendí que para lograrlo debía esforzarme por trasmitir a otros el amor universal, sabiendo que 
              esto sería siempre posible aun cuando me hallase frente a un ser humano inflexible, deprimido, tímido y sin muchas ganar de vivir, pues en él estaría escondida su luz, ya que  todos pertenecemos a esa energía universal de amor.</p>
            <p>En nuestras relaciones, los seres humanos encontramos un sentido de pertenencia: a una familia, a una pareja,  a un círculo social. Pero la relación de pertenencia compartida por todos es la de ser parte de esa
               gran energía universal de amor, fuente creadora de todo lo que existe. Al mirar en mi interior, sentí esa energía integradora y un inmenso amor que, para mí, es el amor de Dios. Fue un sentimiento de totalidad 
               y paz.</p>
            <p>
              Comprender y sentir todo lo que les estoy diciendo, me hizo comprometerme más con mi crecimiento espiritual. Éste es un camino sin retroceso, en el que puedo encontrar traspiés o a veces lentitud en mis aprendizajes, pero nunca la 
              pérdida de ese vínculo con el amor universal iniciado en mi nacimiento. 
            </p>
            <p>
              En nuestra naturaleza está el ser capaces tanto de dar como de recibir amor; debemos permitir que esto ocurra pues el amor debe estar presente siempre entre nosotros. Al iniciar concientemente la travesía de mi crecimiento espiritual, 
              como una consecuencia natural del mismo, fue incrementándose en mí el afán de servir. Es decir, de compartir lo aprendido. Se sirve cuando tratamos que las personas sientan la presencia del amor en alguna de sus diferentes manifestaciones, 
              acompañándolos en el camino del despertar de su conciencia que también es nuestro camino, porque todos somos uno.
            </p>
