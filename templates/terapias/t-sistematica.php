    <h5>Fundamento de la terapia de constelación</h5>
    <p>
        La terapia de la constelación está basada en los Órdenes del Amor de Bert Hellinger. Es una metodología sistémica que, a 
        partir de una situación limitante, explora sus implicaciones para modificarlas y llevar hacia la solución de dichas 
        limitaciones. Esto es, parte de que la resolución de nuestros problemas debe efectuarse a nivel inconsciente.
        Las constelaciones se apoyan en la capacidad del ser humano de proyectar al exterior sus imágenes simbólicas, que son 
        esencia de su concepción interna del mundo. Las constelaciones pueden realizarse tanto en grupo como de manera individual. 
        En ambos casos, se genera un “campo” en el que las situaciones personales proyectadas en imágenes simbólicas se manifiestan 
        a través de personas, en el trabajo de constelación grupal, y de objetos, en la constelación individua
    </p>
    <p>
        Durante el trabajo de constelación, la persona que consulta obtiene una información directa de sensaciones, emociones y 
        creencias. Esto le ayuda para tener la vivencia de su  desorden sistémico y para transformarlo en el mismo momento que va 
        desplegando su constelación.
    </p>
    <h5>
        El trabajo de la constelación individual
    </h5>
    <p>
        El punto de partida para que la persona que consulta descubra y resuelva el conflicto planteado, es la configuración que 
        crea con los objetos que escoge.  Se observa dichos objetos y se tiene en cuenta el motivo de su elección, la distancia 
        entre ellos y su posición relativa, hacia donde se orientan o “miran”, elementos que faltan o sobran, etc. También son 
        importantes las frases que se pronuncien.
        Durante la sesión, en la persona se produce lo siguiente:
        <ul>
            <li>Muestra reconocimiento sobre hechos importantes en su familia.</li>
            <li>Da movimientos incipientes.</li>
            <li>Expresa sentimientos que subyacen en su interior.</li>
            <li>Integra “partes” excluidas.</li>
            <li>Siente que toma el lugar que le corresponde y con esto, su fuerza.</li>
            <li>Agradece por lo que ha aprendido, sanando.</li>
            <li>Expresa una decisión o la voluntad de un próximo paso.</li>
            <li>Da su honra a aquellas personas que corresponden.</li>
            <li>Se despide de algo o de alguien.</li>                    
        </ul>
    </p>
    <h5>Una constelación individual es útil cuando:</h5>
    <ul>
        <li>Se desea explorar más de una situación limitante.</li>
        <li>Se quiere tener privacidad.</li>
        <li>Se siente dificultad en el trabajo en grupo.</li>
        <li>Se espera que sea un complemento de otras modalidades de trabajo individual.</li>
    </ul>
