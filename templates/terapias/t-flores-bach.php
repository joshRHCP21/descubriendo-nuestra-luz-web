    <p>Las distintas situaciones cotidianas y el modo de vida moderno muchas veces hacen que nos sintamos de una manera muy distinta a 
        como necesitamos para disfrutar de una salud plena y que podamos mantener una relación armoniosa con nuestra familia, amigos y 
        todo nuestro entorno.
    </p>
    <p>
        Debido a esto es que algunas veces sentimos que necesitamos un poco de ayuda para afrontar estas situaciones y lograr nuestros 
        objetivos de vida. Se ha comprobado que existe relación entre las emociones y el sistema inmunitario, y esto refuerza el punto 
        de vista de que la salud física y la emocional están vinculadas. Este sistema de Flores de Bach es una herramienta de ayuda 
        eficaz tanto para las causas de las enfermedades como para trastornos pasajeros en nuestras vidas.
    </p>

    <p>
        El doctor Edward Bach, médico inglés, homeópata y bacteriólogo, descubrió este sistema el cual está integrado por 38 remedios 
        florales. De los cuales 37 son flores, y  uno, es agua de manantial (Rock Water). La humanidad se ha beneficiado de su acción 
        sanadora por más de 60 años. Totalmente inocuos y seguros, no crean dependencia, adicciones, ni producen reacciones secundarias. 
        Todos pueden tomar los remedios, a cualquier edad y sin riesgo. Las flores de Bach se utilizan como una terapia complementaria 
        que apoya los tratamientos médicos y psicológicos.
    </p>
    <h6>Este sistema contempla 7 grupos de estados anímicos:</h6>
    <p>
        Grupo 1.- Para tratar los temores <br>
        Grupo 2.- Para tratar la incertidumbre <br>
        Grupo 3.- Para tratar el desinterés en lo actual <br>
        Grupo 4.- Para tratar las manifestaciones de la soledad <br>
        Grupo 5.- Para tratar la susceptibilidad a las influencias y opiniones de los demás. <br>
        Grupo 6.- Para tratar la desesperación y el abatimiento. <br>
        Grupo 7.- Para tratar a aquellos que sufren por los demás <br>
    </p>
    <p>
        Para entender la naturaleza de la enfermedad hay que conocer ciertas verdades fundamentales que Bach considera para los tratamientos:
    </p>
    <p>
        1.- El hombre tiene un Alma que es su ser real, del cual el cuerpo es un reflejo. <br>
        2.- Nosotros, tal y como nos conocemos en el mundo somos Personalidades que estamos aquí para obtener todo el conocimiento y la experiencia.
    </p>
    <p>
        Reconociendo esto, mientras nuestra alma y nuestra personalidad estén en buena armonía, todo es paz y alegría, felicidad y salud. Cuando 
        nuestras personalidades se desvían del camino trazado por el alma, surge el conflicto.
    </p>
    <p>
        El terapeuta, lo que hará es dialogar con el paciente para encontrar los estados anímicos que subyacen en ese ser humano; y así conducirlo 
        a un proceso de encuentro consigo mismo. Las esencias florales, lo que brindan al ser humano, es el desarrollo de la contraparte positiva  
        del estado anímico que lo perturba. Todo ello gracias a las vibraciones de las esencias florales seleccionadas que actúan en el alma.
    </p>
