    <h1>Terapia Bioenergética</h1>  
    <p>En la bioenergética el terapeuta busca integrar aquello que,  en algún momento de la vida de su paciente,  sufrió la pérdida de su armonía, como si 
        se hubiese fracturado.</p>
    <p>
        El terapeuta realiza una sanación emocional. En su práctica utiliza dos enfoques: una observación objetiva y una aproximación subjetiva. Su método es trabajar 
        con la energía vital del paciente. Al medir los cambios logrados en la energía vital puede comprobar en qué medida logró restaurar la desarmonía.
    </p>
    <p>
        Las enfermedades (desarmonías) suelen ser reiteraciones de un mismo conflicto emocional básico ocurrido en la infancia. Por esa razón, este conflicto  debe ser 
        comprendido (aprender la lección) para así evitar que siga repitiéndose en la enfermedad.
    </p>
    <p>
        Sanar se refiere a rescatar la integridad, aprendiendo la lección del evento físico, emocional o mental que originó la desarmonía. Sanarse, por tanto, es aprender; nuestra conciencia aprende la lección que nos traen los conflictos emocionales, luego de ser 
        abordados en la terapia. Esto puede compararse con el sistema inmunológico que aprende la lección de los gérmenes, al defenderse de ellos.
    </p>
    <p>
        Así, se dice que el conflicto emocional se revela como un maestro, pues al comprenderlo se restablece la armonía interior.
    </p>

    <h1>Terapia Sintergética</h1>              
    <p>La sintergética se basa en la retroalimentación con las propias emisiones energéticas, para estimular la inmunidad y el 
        auto reconocimiento</p>
    <p>
        Es una técnica que permite estudiar la respuesta del organismo frente a un estímulo aplicado sobre su cuerpo. Estos 
        estímulos pueden ser filtros, luz y ondas electromagnéticas.
    </p>
    <p>
        Por ejemplo: se utilizan una serie de “filtros biológicos” que al pasarlos sobre el cuerpo del paciente ocasionan un 
        cambio en el pulso cuando hay anomalías en el organismo. Esta variable en el pulso se conoce como Signo Vascular Autonómico (VAS). 
        Cuando el pulso se modifica, el terapeuta puede apreciar si el paciente tiene o no problemas con los neurotransmisores.
    </p>
    <p>
        Los neurotransmisores son químicos producidos por las neuronas del sistema nervioso que intervienen en todas las funciones del organismo 
        mediante la transmisión de impulsos. Estos neurotransmisores pueden ser representados por “los filtros” que contienen una sustancia orgánica 
        o inorgánica que porta información, la que se puede potenciar, aumentar o quitar, con el fin de restablecer el patrón de organización del sistema 
        del paciente.
    </p>            
    <p>
        Algunos de los neurotransmisores utilizados en la terapia son: serotonina, melatonina, dopamina, melanina, acetilcolina, adrenalina, y vasopresina; 
        todos ellos importantes para el buen funcionamiento del organismo humano.
    </p>    
